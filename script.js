// Функция для отображения одноклассников
function displayClassmates(classmatesToDisplay = classmates) {
  container.innerHTML = '';
  classmatesToDisplay.forEach(classmate => {
    const card = document.createElement("div");
    card.classList.add("card");

    const favoriteButton = document.createElement('button');
    favoriteButton.classList.add('favorite-button');
    favoriteButton.innerHTML = classmate.isFavorite ? '★' : '☆';
    favoriteButton.addEventListener('click', (e) => {
      e.stopPropagation();
      classmate.isFavorite = !classmate.isFavorite;
      favoriteButton.innerHTML = classmate.isFavorite ? '★' : '☆';
    });

    card.innerHTML = `
        <img src="${classmate.photo}" alt="${classmate.name}">
        <h3>${classmate.name}</h3>
        <p>${classmate.description}</p>
      `;
    card.appendChild(favoriteButton);
    container.appendChild(card);

    card.addEventListener('click', function () {
      openModal(classmate)
    });

    card.style.opacity = 0;
    card.style.transform = 'translateY(20px)';
    setTimeout(() => {
      card.style.opacity = 1;
      card.style.transform = 'translateY(0)';
      card.style.transition = 'opacity 0.3s ease, transform 0.3s ease';
    }, 50 * classmatesToDisplay.indexOf(classmate));
  });
}
